// Generate packages/db-schema/seed/pesantren_aqidatul_full.sql — the COMPLETE
// 57-bait Aqidatul Awam (Arab + terjemah Indonesia), replacing the earlier
// condensed sample so this kitab reads as a full, tidy digital book.
//
// Source of the paired Arabic matn + Indonesian translation: the open
// wahyu9kdl/islami "Kitab Kuning" project (GPL-3.0). The Arabic nadhom itself
// is a classical public-domain text by Syaikh Ahmad Al-Marzuqi (d. 1281H).
// Nothing here is recalled from memory — misquoting sacred/classical text is
// never acceptable; every bait is carried verbatim from the vetted source and
// only whitespace/entities were normalised.
//
// The generated SQL is IDEMPOTENT and self-upgrading: it clears any existing
// aqidatulawam chapters/matan first, then re-inserts the full structured
// version. The deploy step guards it with a matan-count check so it only runs
// while the kitab is still the old condensed one. Run:
//   node scripts/generate-aqidatul-full-seed.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.join(__dirname, "data", "aqidatul-awam-full.json");
const OUT = path.join(__dirname, "..", "packages", "db-schema", "seed", "pesantren_aqidatul_full.sql");

const q = (v) => (v == null ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);

const k = JSON.parse(fs.readFileSync(SRC, "utf8"));
const lines = [];
lines.push("-- Aqidatul Awam — COMPLETE 57-bait matn (Arab + terjemah).");
lines.push("-- GENERATED by scripts/generate-aqidatul-full-seed.mjs. DO NOT edit by hand.");
lines.push("-- Idempotent: clears old chapters/matan for this kitab, then re-inserts full text.");
lines.push("PRAGMA foreign_keys = OFF;");
lines.push("");

// 1) Make sure the kitab row exists with correct, complete metadata.
lines.push(
  `INSERT OR IGNORE INTO pesantren_kitab (slug, category_slug, title_ar, title_id, author, author_death_year, description_id, sort_order) ` +
    `VALUES (${q(k.slug)}, ${q(k.category_slug)}, ${q(k.title_ar)}, ${q(k.title_id)}, ${q(k.author)}, ${q(k.author_death_year)}, ${q(k.desc)}, 5);`
);
lines.push(
  `UPDATE pesantren_kitab SET description_id = ${q(k.desc)}, author = ${q(k.author)}, author_death_year = ${q(k.author_death_year)} WHERE slug = ${q(k.slug)};`
);
lines.push("");

// 2) Clear the old condensed chapters + their matan (idempotent upgrade).
lines.push(
  `DELETE FROM pesantren_matn WHERE bab_id IN (SELECT id FROM pesantren_bab WHERE kitab_slug = ${q(k.slug)});`
);
lines.push(`DELETE FROM pesantren_bab WHERE kitab_slug = ${q(k.slug)};`);
lines.push("");

// 3) Insert the full, structured version: 9 chapters, each bait a matan row.
let babOrder = 0;
let matnCount = 0;
for (const bab of k.babs) {
  babOrder++;
  lines.push(
    `INSERT INTO pesantren_bab (kitab_slug, bab_order, name_id, name_ar) VALUES (${q(k.slug)}, ${babOrder}, ${q(bab.name)}, ${q(bab.ar)});`
  );
  bab.matn.forEach((m, mi) => {
    matnCount++;
    lines.push(
      `INSERT INTO pesantren_matn (bab_id, matn_order, title_id, text_ar, translation_id) ` +
        `SELECT id, ${mi + 1}, ${q("Bait " + m.n)}, ${q(m.ar)}, ${q(m.tr)} ` +
        `FROM pesantren_bab WHERE kitab_slug = ${q(k.slug)} AND bab_order = ${babOrder};`
    );
  });
  lines.push("");
}

fs.writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`Wrote ${OUT}`);
console.log(`  chapters: ${babOrder}, matan (bait): ${matnCount}`);
