// Generate packages/db-schema/seed/hadith_dehlawi.sql — the 40 Hadith of Shah
// Waliullah Ad-Dehlawi (Forty Hadith of Shah Waliullah Dehlawi), Arabic +
// English (Indonesian translated on demand, same pattern as Arba'in Nawawi /
// Hadits Qudsi). Source: fawazahmed0/hadith-api (github.com/fawazahmed0/
// hadith-api), Unlicense / public domain — the SAME properly-licensed source
// already used for every other hadith collection in this project.
//
// Reads the per-hadith JSON files already checked out at
// <CLONE_DIR>/editions/{ara,eng}-dehlawi/<n>.json and writes one seed file.
// Run: node scripts/generate-hadith-dehlawi-seed.mjs <path-to-hadith-api-clone>

import fs from "node:fs";
import path from "node:path";

const CLONE_DIR = process.argv[2];
if (!CLONE_DIR) {
  console.error("Usage: node scripts/generate-hadith-dehlawi-seed.mjs <path-to-hadith-api-clone>");
  process.exit(1);
}
const OUT = path.join(process.cwd(), "packages", "db-schema", "seed", "hadith_dehlawi.sql");

const q = (v) => (v == null ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);

const ID_START = 80001; // next free block after qudsi (75001-79999)

const lines = [];
lines.push("-- Auto-generated by scripts/generate-hadith-dehlawi-seed.mjs — DO NOT hand-edit.");
lines.push("-- Source: fawazahmed0/hadith-api (github.com/fawazahmed0/hadith-api), Unlicense / public domain.");
lines.push("-- 40 Hadith of Shah Waliullah Ad-Dehlawi — Arabic + English (Indonesian translated on demand).");
lines.push("-- Bulk-imported rows leave narrator NULL — the translated text already includes the full isnad.");
lines.push("");
lines.push("INSERT OR REPLACE INTO hadits_collection (slug, name_id, name_ar, author, sort_order, id_start, id_end, has_native_id) VALUES");
lines.push(`  ('dehlawi', 'Arba''in Ad-Dehlawi (40 Hadits Syah Waliullah)', 'الأربعون للشاه ولي الله الدهلوي', 'Syah Waliullah Ad-Dehlawi', 10, ${ID_START}, ${ID_START + 998}, 0);`);
lines.push("");
lines.push("INSERT OR IGNORE INTO hadits (id, text_ar, text_id, text_en, narrator, grade, source, collection, hadith_number) VALUES");

const rows = [];
for (let n = 1; n <= 40; n++) {
  const ar = JSON.parse(fs.readFileSync(path.join(CLONE_DIR, "editions", "ara-dehlawi", `${n}.json`), "utf8"));
  const en = JSON.parse(fs.readFileSync(path.join(CLONE_DIR, "editions", "eng-dehlawi", `${n}.json`), "utf8"));
  const arH = ar.hadiths[0];
  const enH = en.hadiths[0];
  const grade = Array.isArray(enH.grades) && enH.grades.length ? enH.grades.map((g) => g.grade).join("; ") : null;
  const id = ID_START + n - 1;
  rows.push(
    `  (${id}, ${q(arH.text)}, ${q(enH.text)}, ${q(enH.text)}, NULL, ${q(grade)}, ${q(`Arba'in Ad-Dehlawi no. ${n}`)}, 'dehlawi', ${n})`
  );
}
lines.push(rows.join(",\n") + ";");
lines.push("");

fs.writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`Wrote ${OUT} — ${rows.length} hadith`);
