// Generate packages/db-schema/seed/nasakh_mansukh.sql from the curated
// Nasakh & Mansukh dataset. Run: node scripts/generate-nasakh-seed.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.join(__dirname, "data", "nasakh-mansukh.json");
const OUT = path.join(__dirname, "..", "packages", "db-schema", "seed", "nasakh_mansukh.sql");

const q = (v) => (v == null || v === "" ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);

const { entries } = JSON.parse(fs.readFileSync(SRC, "utf8"));

const lines = [];
lines.push("-- Nasakh & Mansukh — GENERATED by scripts/generate-nasakh-seed.mjs. DO NOT edit by hand.");
lines.push("PRAGMA foreign_keys = OFF;");
lines.push("");

entries.forEach((e, i) => {
  lines.push(
    `INSERT OR IGNORE INTO nasakh_mansukh (entry_order, title_id, naskh_type, mansukh_ref, mansukh_ar, mansukh_id, nasikh_ref, nasikh_ar, nasikh_id, explanation_id, source) VALUES (` +
      `${i + 1}, ${q(e.title)}, ${q(e.type)}, ${q(e.mansukh_ref)}, ${q(e.mansukh_ar)}, ${q(e.mansukh_id)}, ${q(e.nasikh_ref)}, ${q(e.nasikh_ar)}, ${q(e.nasikh_id)}, ${q(e.explanation)}, ${q(e.source)});`
  );
});

fs.writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`Wrote ${OUT} — ${entries.length} entries`);
