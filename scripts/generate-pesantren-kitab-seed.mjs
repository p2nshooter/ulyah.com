// Generate packages/db-schema/seed/pesantren_kitab.sql from the curated
// "Kitab Pesantren Digital" dataset (13 classic matn: Safinatun Najah,
// Ta'lim Muta'allim, Al-Jurumiyyah, Aqidatul Awam, Alfiyah, Bidayatul
// Hidayah, Sullam Taufiq, Fathul Qarib, Minhajut Thalibin, Tijan Daruri,
// Nashaihud Diniyyah, Al-Imrithi, Umm al-Barahin), organised category →
// kitab → bab → matan. Run: node scripts/generate-pesantren-kitab-seed.mjs
//
// Source JSON is the extracted KITABS object; author death-years are parsed
// out of the author string where present ("… (w.723H)").

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.join(__dirname, "data", "pesantren-kitabs.json");
const OUT = path.join(__dirname, "..", "packages", "db-schema", "seed", "pesantren_kitab.sql");

const CATEGORY_META = {
  Fiqih: { slug: "fiqih", ar: "الفقه", icon: "⚖️" },
  Akidah: { slug: "akidah", ar: "العقيدة", icon: "☪️" },
  Akhlak: { slug: "akhlak", ar: "الأخلاق والتصوف", icon: "🤲" },
  "Nahwu-Shorof": { slug: "nahwu-shorof", ar: "النحو والصرف", icon: "✍️" },
};

const q = (v) => (v == null ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);

function splitAuthor(auth) {
  if (!auth) return { name: null, death: null };
  const m = auth.match(/\(w\.?\s*([0-9]+\s*H?)\)/i);
  const death = m ? m[1].replace(/\s+/g, "") : null;
  const name = auth.replace(/\s*\(w\.?[^)]*\)\s*/i, "").trim();
  return { name: name || auth, death };
}

const { cats, data } = JSON.parse(fs.readFileSync(SRC, "utf8"));

// Map each kitab id -> its category name (from cats[].ids)
const kitabCategory = {};
for (const c of cats) for (const id of c.ids) kitabCategory[id] = c.name;

const lines = [];
lines.push("-- Kitab Pesantren readable library — GENERATED by");
lines.push("-- scripts/generate-pesantren-kitab-seed.mjs. DO NOT edit by hand.");
lines.push("PRAGMA foreign_keys = OFF;");
lines.push("");

// Categories
for (const c of cats) {
  const meta = CATEGORY_META[c.name];
  if (!meta) throw new Error(`No CATEGORY_META for "${c.name}"`);
  const order = cats.indexOf(c) + 1;
  lines.push(
    `INSERT OR IGNORE INTO pesantren_category (slug, name_id, name_ar, icon, sort_order) VALUES (${q(meta.slug)}, ${q(c.name)}, ${q(meta.ar)}, ${q(meta.icon)}, ${order});`
  );
}
lines.push("");

let kitabCount = 0;
let babCount = 0;
let matnCount = 0;

// Kitabs, in the category-declared order so the library reads tidily.
let kitabOrder = 0;
for (const c of cats) {
  const meta = CATEGORY_META[c.name];
  for (const kid of c.ids) {
    const k = data[kid];
    if (!k) {
      console.warn(`WARN: kitab id "${kid}" listed in category but has no data — skipped`);
      continue;
    }
    kitabOrder++;
    kitabCount++;
    const { name: authorName, death } = splitAuthor(k.auth);
    lines.push(
      `INSERT OR IGNORE INTO pesantren_kitab (slug, category_slug, title_ar, title_id, author, author_death_year, description_id, sort_order) VALUES (${q(kid)}, ${q(meta.slug)}, ${q(k.ar)}, ${q(k.id)}, ${q(authorName)}, ${q(death)}, ${q(k.desc)}, ${kitabOrder});`
    );

    (k.babs || []).forEach((bab, bi) => {
      babCount++;
      const babOrder = bi + 1;
      // Chapters reference their kitab by slug; matan rows resolve bab_id via a
      // subselect so this seed needs no hard-coded autoincrement ids.
      lines.push(
        `INSERT OR IGNORE INTO pesantren_bab (kitab_slug, bab_order, name_id, name_ar) VALUES (${q(kid)}, ${babOrder}, ${q(bab.name)}, ${q(bab.ar)});`
      );
      (bab.matn || []).forEach((m, mi) => {
        matnCount++;
        const matnOrder = mi + 1;
        const quran = m.quran && m.quran.length ? JSON.stringify(m.quran) : null;
        const hadits = m.hadits && m.hadits.length ? JSON.stringify(m.hadits) : null;
        lines.push(
          `INSERT OR IGNORE INTO pesantren_matn (bab_id, matn_order, title_id, title_ar, text_ar, translation_id, explanation_id, quran_refs_json, hadits_refs_json) ` +
            `SELECT id, ${matnOrder}, ${q(m.t)}, ${q(m.tAr)}, ${q(m.ar)}, ${q(m.tr)}, ${q(m.tf)}, ${q(quran)}, ${q(hadits)} ` +
            `FROM pesantren_bab WHERE kitab_slug = ${q(kid)} AND bab_order = ${babOrder};`
        );
      });
    });
    lines.push("");
  }
}

fs.writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`Wrote ${OUT}`);
console.log(`  categories: ${cats.length}, kitab: ${kitabCount}, bab: ${babCount}, matan: ${matnCount}`);
