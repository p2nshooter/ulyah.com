// Generate packages/db-schema/seed/amalan_harian.sql from the curated
// Amalan Harian dataset (doa bangun→tidur, dzikir, thibbun nabawi, fitrah &
// keindahan). Each item: Arabic + Latin + terjemah + catatan + sumber sahih.
// Run: node scripts/generate-amalan-seed.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.join(__dirname, "data", "amalan-harian.json");
const OUT = path.join(__dirname, "..", "packages", "db-schema", "seed", "amalan_harian.sql");

const q = (v) => (v == null || v === "" ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);

const { categories, items } = JSON.parse(fs.readFileSync(SRC, "utf8"));

const lines = [];
lines.push("-- Amalan Harian (doa, dzikir, thibbun nabawi, fitrah) — GENERATED by");
lines.push("-- scripts/generate-amalan-seed.mjs. DO NOT edit by hand.");
lines.push("PRAGMA foreign_keys = OFF;");
lines.push("");

for (const c of categories) {
  lines.push(
    `INSERT OR IGNORE INTO amalan_category (slug, grp, name_id, name_ar, icon, sort_order) VALUES (${q(c.slug)}, ${q(c.grp)}, ${q(c.name_id)}, ${q(c.name_ar)}, ${q(c.icon)}, ${c.sort});`
  );
}
lines.push("");

let itemCount = 0;
for (const c of categories) {
  const list = items[c.slug] || [];
  list.forEach((it, i) => {
    itemCount++;
    lines.push(
      `INSERT OR IGNORE INTO amalan_item (category_slug, item_order, title_id, arabic, latin, translation_id, note_id, repeat_count, source) VALUES (` +
        `${q(c.slug)}, ${i + 1}, ${q(it.title)}, ${q(it.ar)}, ${q(it.latin)}, ${q(it.tr)}, ${q(it.note)}, ${it.repeat ?? "NULL"}, ${q(it.source)});`
    );
  });
}

fs.writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`Wrote ${OUT}`);
console.log(`  categories: ${categories.length}, items: ${itemCount}`);
