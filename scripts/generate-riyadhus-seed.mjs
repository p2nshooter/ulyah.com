// Generate packages/db-schema/seed/hadith_riyadhus.sql — Riyadhus Shalihin
// (Imam An-Nawawi), 372 hadith with Arabic + Indonesian translation.
//
// Source: irsyadulibad/hadits-database → riyadhus-shalihin.sql (data from
// carihadis.com). The upstream table is `riyadhus_shalihin(id, kitab, arab,
// terjemah)`; the parsed, HTML-stripped tuples live in
// scripts/data/riyadhus-shalihin.json. Nothing is recalled from memory —
// every hadith is carried verbatim from the vetted source.
//
// One INSERT statement per hadith (never a single mega-statement) so no line
// exceeds D1's ~100 KB per-statement limit (SQLITE_TOOBIG). Editorial notes
// that leaked into a couple of translations (a blog URL "Silahkan kunjungi
// http…") are stripped so only the hadith text remains. Run:
//   node scripts/generate-riyadhus-seed.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.join(__dirname, "data", "riyadhus-shalihin.json");
const OUT = path.join(__dirname, "..", "packages", "db-schema", "seed", "hadith_riyadhus.sql");

const ID_BASE = 100000; // hadith id = ID_BASE + source id (100001..100372)

// D1 rejects any single SQL statement over ~100 KB (SQLITE_TOOBIG). A couple
// of upstream rows are not single hadith but an entire appendix chapter
// concatenated into one field (dozens of hadith at once). They cannot be split
// into aligned Arabic/translation pairs safely — the internal hadith-number
// markers don't match 1:1 between the two languages — so rather than risk
// pairing the wrong Arabic with the wrong terjemah (never acceptable for
// sacred text), any statement that would exceed this budget is skipped and
// logged. Everything that IS a clean single hadith is kept.
const MAX_STMT_BYTES = 95000;

const q = (v) => (v == null ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);

// Remove editorial cruft that leaked into a few translations without touching
// the hadith wording itself: trailing "Silahkan kunjungi <url> …" notes and
// any bare URLs, plus stray backticks / collapsed whitespace.
function scrub(text) {
  let t = String(text);
  t = t.replace(/Silahkan kunjungi\s+https?:\/\/\S+[^.]*\.?/gi, "");
  t = t.replace(/https?:\/\/\S+/gi, "");
  t = t.replace(/`/g, "");
  t = t.replace(/\s+/g, " ").trim();
  return t;
}

const rows = JSON.parse(fs.readFileSync(SRC, "utf8")).sort((a, b) => a.src_id - b.src_id);

const lines = [];
lines.push("-- Riyadhus Shalihin (Imam An-Nawawi) — 372 hadith, Arabic + Indonesian.");
lines.push("-- GENERATED by scripts/generate-riyadhus-seed.mjs. DO NOT edit by hand.");
lines.push("-- Source: irsyadulibad/hadits-database (data from carihadis.com), HTML-stripped,");
lines.push("-- one INSERT per hadith (D1 per-statement size safe), editorial URLs removed.");
lines.push(
  "INSERT OR IGNORE INTO hadits_collection (slug, name_id, name_ar, author, sort_order, id_start, id_end, has_native_id) VALUES"
);
lines.push(
  `  ('riyadhus', 'Riyadhus Shalihin', 'رياض الصالحين', 'Imam An-Nawawi', 12, ${ID_BASE + 1}, ${ID_BASE + rows.length}, 1);`
);
lines.push("");

let count = 0;
let maxBytes = 0;
const skipped = [];
for (const r of rows) {
  const ar = scrub(r.ar);
  const tr = scrub(r.tr);
  if (!ar || !tr) continue;
  const id = ID_BASE + r.src_id;
  const stmt =
    `INSERT OR IGNORE INTO hadits (id, text_ar, text_id, narrator, grade, source, collection, hadith_number) ` +
    `VALUES (${id}, ${q(ar)}, ${q(tr)}, NULL, NULL, 'Riyadhus Shalihin', 'riyadhus', ${r.src_id});`;
  const bytes = Buffer.byteLength(stmt, "utf8");
  if (bytes > MAX_STMT_BYTES) {
    skipped.push({ src_id: r.src_id, bytes });
    continue;
  }
  count++;
  maxBytes = Math.max(maxBytes, bytes);
  lines.push(stmt);
}

fs.writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`Wrote ${OUT}`);
console.log(`  hadith: ${count}, largest statement: ${maxBytes} bytes (D1 limit ~100 KB)`);
if (skipped.length) {
  console.log(`  skipped ${skipped.length} oversized concatenated-chapter row(s): ` + skipped.map((s) => `#${s.src_id} (${s.bytes}B)`).join(", "));
}
